#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*copy;
	int		i;
	int		s;

	i = 0;
	s = 0;
	while (src[s] != '\0')
		s++;
	copy = malloc(sizeof(char) * (s + 1));
	if (!copy)
		return (NULL);
	while (src[i] != '\0')
	{
		copy[i] = src[i];
		i++;
	}
	copy[i] = '\0';
	return (copy);
}
